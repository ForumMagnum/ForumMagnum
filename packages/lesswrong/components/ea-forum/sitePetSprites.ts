const fillInteriorPixels = (grid: SitePetSpriteGridValue[][]): SitePetSpriteGridValue[][] => {
  const height = grid.length;
  const width = grid[0]?.length ?? 0;
  const visited = Array.from({length: height}, () => Array(width).fill(false));
  const queue: Array<{x: number, y: number}> = [];

  const enqueueExteriorCell = (x: number, y: number) => {
    if (x < 0 || x >= width || y < 0 || y >= height) {
      return;
    }

    if (visited[y][x] || grid[y][x] !== 0) {
      return;
    }

    visited[y][x] = true;
    queue.push({x, y});
  };

  for (let x = 0; x < width; x++) {
    enqueueExteriorCell(x, 0);
    enqueueExteriorCell(x, height - 1);
  }

  for (let y = 0; y < height; y++) {
    enqueueExteriorCell(0, y);
    enqueueExteriorCell(width - 1, y);
  }

  while (queue.length > 0) {
    const current = queue.shift();
    if (!current) {
      continue;
    }

    enqueueExteriorCell(current.x + 1, current.y);
    enqueueExteriorCell(current.x - 1, current.y);
    enqueueExteriorCell(current.x, current.y + 1);
    enqueueExteriorCell(current.x, current.y - 1);
  }

  return grid.map((row, y) =>
    row.map((value, x) => (value === 0 && !visited[y][x]) ? 4 : value)
  );
};

const fillAnimationFrames = (frames: SitePetSpriteGridValue[][][]) => frames.map(fillInteriorPixels);
