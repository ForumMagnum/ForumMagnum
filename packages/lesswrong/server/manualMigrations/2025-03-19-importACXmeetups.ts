export async function testSlugs() {
  for (const meetup of acxData) {
    if (!meetup.Username) {
      continue;
    }
    const maybeSlug = meetup.Username.toLowerCase();
    const user = await Users.findOne({ $or: [{slug: maybeSlug}, {oldSlugs: maybeSlug}] });
    if (user) {
      const emailsMatch = user.email === meetup['Email address'];
      if (!emailsMatch) {
        console.log(`User with listed Username ${meetup.Username} exists with slug ${maybeSlug}.`);
        console.log(`--- Emails do not match: ${user.email} !== ${meetup['Email address']}`);
      }
    } else {
      console.log(`User with listed Username ${meetup.Username} does not exist with slug ${maybeSlug}.`);
    }

    console.log('');
    console.log('--- --- ---');
    console.log('');
  }
}
